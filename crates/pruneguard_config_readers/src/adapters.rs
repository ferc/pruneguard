/// Confidence level for an adapter's outputs.
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]
pub enum AdapterConfidence {
    #[default]
    High,
    Medium,
    Low,
}

/// A generated entrypoint discovered from framework build artifacts.
#[derive(Debug, Clone, Default)]
pub struct GeneratedEntrypoint {
    pub path: PathBuf,
    pub kind: String,
    pub reason: String,
}

/// A glob pattern for route-generated entrypoints.
#[derive(Debug, Clone, Default)]
pub struct RouteEntryGlob {
    pub pattern: String,
    pub framework: String,
    pub reason: String,
}

/// A synthetic import map extracted from a generated `.d.ts` file.
#[derive(Debug, Clone, Default)]
pub struct SyntheticImportMap {
    pub source_file: PathBuf,
    pub mappings: Vec<SyntheticMapping>,
    pub framework: String,
}

/// A single mapping from a synthetic import map.
#[derive(Debug, Clone, Default)]
pub struct SyntheticMapping {
    pub import_name: String,
    pub resolved_path: Option<String>,
    pub is_type: bool,
}

/// A reason for low confidence in a specific area of extraction.
#[derive(Debug, Clone)]
pub struct LowConfidenceReason {
    pub scope: String,
    pub reason: String,
}

/// An alias generated by a framework build process (e.g. Nuxt auto-imports,
/// Nitro server utilities) that should be treated as resolvable.
#[derive(Debug, Clone)]
pub struct GeneratedAlias {
    pub alias: String,
    pub target: String,
    pub framework: String,
}

/// A root directory that backs a virtual module prefix.
#[derive(Debug, Clone)]
pub struct VirtualModuleRoot {
    pub prefix: String,
    pub root_dir: PathBuf,
    pub framework: String,
}

/// Structured inputs extracted from framework configuration files.
/// These feed into the graph builder to improve entrypoint detection,
/// alias resolution, and dependency tracking.
#[derive(Debug, Clone, Default)]
pub struct ConfigInputs {
    /// Path aliases discovered from config (e.g. webpack resolve.alias, vite resolve.alias).
    pub aliases: Vec<AliasEntry>,
    /// Additional entrypoint paths discovered from config.
    pub entrypoints: Vec<PathBuf>,
    /// Directories to include as source roots.
    pub source_roots: Vec<PathBuf>,
    /// Patterns for files to ignore.
    pub ignore_patterns: Vec<String>,
    /// External packages (should not be resolved as local).
    pub externals: Vec<String>,
    /// Test file patterns discovered from config.
    pub test_patterns: Vec<String>,
    /// Framework name that this config belongs to.
    pub framework: Option<String>,
    /// Runtime entrypoints (e.g. server entry, worker entry).
    pub runtime_entrypoints: Vec<PathBuf>,
    /// Production-only entrypoints.
    pub production_entrypoints: Vec<PathBuf>,
    /// Development-only entrypoints (e.g. dev server entry).
    pub development_entrypoints: Vec<PathBuf>,
    /// Story file entrypoints.
    pub story_entrypoints: Vec<PathBuf>,
    /// Setup files (e.g. jest setup, vitest setup).
    pub setup_files: Vec<PathBuf>,
    /// Global setup files.
    pub global_setup_files: Vec<PathBuf>,
    /// Auto-import root directories (e.g. Nuxt composables/).
    pub auto_import_roots: Vec<PathBuf>,
    /// Confidence level for this adapter's outputs.
    pub confidence: AdapterConfidence,
    /// Specifier patterns to treat as resolved (e.g. virtual modules, framework aliases).
    pub ignore_unresolved: Vec<String>,
    /// Generated entrypoints from framework build artifacts (e.g. .nuxt/imports.d.ts).
    pub generated_entrypoints: Vec<GeneratedEntrypoint>,
    /// Route-generated entry globs for frameworks like `TanStack` Router, React Router.
    pub route_entry_globs: Vec<RouteEntryGlob>,
    /// Synthetic import maps from generated .d.ts files.
    pub synthetic_import_maps: Vec<SyntheticImportMap>,
    /// Virtual module prefixes that should not be treated as unresolved.
    pub virtual_module_prefixes: Vec<String>,
    /// Reasons for low confidence in specific areas.
    pub low_confidence_reasons: Vec<LowConfidenceReason>,
    /// Aliases generated by framework build processes.
    pub generated_aliases: Vec<GeneratedAlias>,
    /// Root directories for virtual modules (e.g. `#imports` → `composables/`).
    pub virtual_module_roots: Vec<VirtualModuleRoot>,
}

/// A path alias mapping (e.g. `@/` -> `./src/`).
#[derive(Debug, Clone, Default)]
pub struct AliasEntry {
    pub pattern: String,
    pub target: String,
    /// Which config source this alias came from.
    pub origin: AliasOrigin,
}

impl AliasEntry {
    const fn new(pattern: String, target: String, origin: AliasOrigin) -> Self {
        Self { pattern, target, origin }
    }
}

impl ConfigInputs {
    /// Merge another `ConfigInputs` into this one, combining all fields.
    pub fn merge(&mut self, other: Self) {
        self.aliases.extend(other.aliases);
        self.entrypoints.extend(other.entrypoints);
        self.source_roots.extend(other.source_roots);
        self.ignore_patterns.extend(other.ignore_patterns);
        self.externals.extend(other.externals);
        self.test_patterns.extend(other.test_patterns);
        // Keep the first framework name we see; don't overwrite with None.
        if self.framework.is_none() {
            self.framework = other.framework;
        }
        self.runtime_entrypoints.extend(other.runtime_entrypoints);
        self.production_entrypoints.extend(other.production_entrypoints);
        self.development_entrypoints.extend(other.development_entrypoints);
        self.story_entrypoints.extend(other.story_entrypoints);
        self.setup_files.extend(other.setup_files);
        self.global_setup_files.extend(other.global_setup_files);
        self.auto_import_roots.extend(other.auto_import_roots);
        self.ignore_unresolved.extend(other.ignore_unresolved);
        self.generated_entrypoints.extend(other.generated_entrypoints);
        self.route_entry_globs.extend(other.route_entry_globs);
        self.synthetic_import_maps.extend(other.synthetic_import_maps);
        self.virtual_module_prefixes.extend(other.virtual_module_prefixes);
        self.low_confidence_reasons.extend(other.low_confidence_reasons);
        self.generated_aliases.extend(other.generated_aliases);
        self.virtual_module_roots.extend(other.virtual_module_roots);
        // Keep the first non-default confidence.
        if self.confidence == AdapterConfidence::High && other.confidence != AdapterConfidence::High
        {
            self.confidence = other.confidence;
        }
    }
}

